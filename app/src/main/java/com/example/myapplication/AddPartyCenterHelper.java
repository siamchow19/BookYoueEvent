package com.example.myapplication;

public class AddPartyCenterHelper {
    String division;
    String district;
    String policeStation;
    String partyCenterName;
    String partyCenterAddress;
    String termsAndPolicies;
    String capacity;
    String startingPrice;
    String imageUrl;
    String partyCenterId;
    String ownerId;
    String partyCenterStatus;
    String email;
    String contactNo;

    public AddPartyCenterHelper() {
    }

    public AddPartyCenterHelper(String division, String district, String policeStation, String partyCenterName, String partyCenterAddress, String termsAndPolicies, String capacity, String startingPrice, String imageUrl, String partyCenterId, String ownerId, String partyCenterStatus, String email, String contactNo) {
        this.division = division;
        this.district = district;
        this.policeStation = policeStation;
        this.partyCenterName = partyCenterName;
        this.partyCenterAddress = partyCenterAddress;
        this.termsAndPolicies = termsAndPolicies;
        this.capacity = capacity;
        this.startingPrice = startingPrice;
        this.imageUrl = imageUrl;
        this.partyCenterId = partyCenterId;
        this.ownerId = ownerId;
        this.partyCenterStatus = partyCenterStatus;
        this.email = email;
        this.contactNo = contactNo;
    }

    public String getDivision() {
        return division;
    }

    public void setDivision(String division) {
        this.division = division;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getPoliceStation() {
        return policeStation;
    }

    public void setPoliceStation(String policeStation) {
        this.policeStation = policeStation;
    }

    public String getPartyCenterName() {
        return partyCenterName;
    }

    public void setPartyCenterName(String partyCenterName) {
        this.partyCenterName = partyCenterName;
    }

    public String getPartyCenterAddress() {
        return partyCenterAddress;
    }

    public void setPartyCenterAddress(String partyCenterAddress) {
        this.partyCenterAddress = partyCenterAddress;
    }

    public String getTermsAndPolicies() {
        return termsAndPolicies;
    }

    public void setTermsAndPolicies(String termsAndPolicies) {
        this.termsAndPolicies = termsAndPolicies;
    }

    public String getCapacity() {
        return capacity;
    }

    public void setCapacity(String capacity) {
        this.capacity = capacity;
    }

    public String getStartingPrice() {
        return startingPrice;
    }

    public void setStartingPrice(String startingPrice) {
        this.startingPrice = startingPrice;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getPartyCenterId() {
        return partyCenterId;
    }

    public void setPartyCenterId(String partyCenterId) {
        this.partyCenterId = partyCenterId;
    }

    public String getOwnerId() {
        return ownerId;
    }

    public void setOwnerId(String ownerId) {
        this.ownerId = ownerId;
    }

    public String getPartyCenterStatus() {
        return partyCenterStatus;
    }

    public void setPartyCenterStatus(String partyCenterStatus) {
        this.partyCenterStatus = partyCenterStatus;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getContactNo() {
        return contactNo;
    }

    public void setContactNo(String contactNo) {
        this.contactNo = contactNo;
    }
}

